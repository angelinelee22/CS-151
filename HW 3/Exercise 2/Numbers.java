public class Numbers {
    // variables
    private int red;
    private int green;
    private int blue;

    public Numbers(int red, int green, int blue){
        this.red = red;
        this.green = green;
        this.blue = blue;
    }

    // getters
    public int getRed() {
        return red;
    }
    public int getGreen() {
        return green;
    }
    public int getBlue() {
        return blue;
    }

    // setters
    public void setRed(int red) {
        this.red = red;
    }
    public void setGreen(int green) {
        this.green = green;
    }
    public void setBlue(int blue) {
        this.blue = blue;
    }

}